        #◇┐ダメージを与える
        if argダメージ == 0:
        #　├┐（クリティカルヒット指定がある場合）
            #↓
            #○シールドをゼロにする
            self.個体.情報.シールド = 0
            #┴
        else:
        #　└┐（その他）
            #↓
            #○ダメージを与える
            self.個体.情報.シールド -= argダメージ
            #┴
        #│
        #◇┐爆破する
        x = self.個体.情報.X
        y = self.個体.情報.Y
