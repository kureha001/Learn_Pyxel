class 本体:
    #┌───────────────────────────────────
    #│初期化
    #└───────────────────────────────────
    def __init__(self, 引数_GAME): #① ゲームのオブジェクト

        #┬
        #≫参照先を用意する
        self.参照 = 参照クラス(引数_GAME)
        #│
        #〇≫データセットを用意する
        self.仕様   = 仕様クラス()
        self.情報   = 情報クラス()
        #│
        #〇処理セットを用意する
        self.FN移動 = 移動クラス(self)
        self.FN描画 = 描画クラス(self)
