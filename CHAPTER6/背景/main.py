#┃仕様
#┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class 仕様クラス:

    星数 = 30

#┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#┃情報
#┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class 情報クラス:

    def __init__(self):

        self.星 = []        # 星リスト

        #◎└┐すべての星を追加する
        for cnt in range(仕様クラス.星数):
            #│＼（すべての星を処理し終えた場合）
            #│ ▼繰り返し処理を抜ける
            #│
            #○位置をセットする
            座標X = pyxel.rndi(0, pyxel.width  - 1  )  
            座標Y = pyxel.rndi(0, pyxel.height - 1  )  
            #│
            #○速度をセットする
            速度Y = pyxel.rndf(0.5, 0.8             ) 
            #│
            #○星を追加する
            self.星.append((座標X, 座標Y, 速度Y))
        #┴　┴

#┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
#┃メイン
